// One-off generator for src/constants/zh-fold-map.ts â€” NOT run at build time.
//
// Produces a single-character Traditionalâ†’Simplified fold map by merging OpenCC's
// Hong-Kong, Taiwan, and Taiwan-phrase Traditionalâ†’Simplified converters, so a title
// stored in ANY Traditional variant (HK or TW) folds to the same Simplified form. The
// runtime `foldZh` (src/lib/zh-fold.ts) uses this map to make local search variant-agnostic
// without loading the ~1MB opencc-js dictionary on the keystroke path.
//
// Regenerate after an opencc-js upgrade:  node scripts/gen-zh-fold-map.mjs
//
// The iterated code-point ranges intentionally match `containsCjk` in src/lib/cjk.ts
// (U+3400â€“U+9FFF and U+F900â€“U+FAFF).

import * as OpenCC from 'opencc-js'
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = join(__dirname, '..', 'src', 'constants', 'zh-fold-map.ts')

// Every Traditional variant â†’ Simplified. Listed lowest-priority first so the generic
// HK/TW base mappings win and the phrase variant only fills gaps.
const toSimplified = ['twp', 'tw', 'hk'].map((from) =>
  OpenCC.Converter({ from, to: 'cn' }),
)

const RANGES = [
  [0x3400, 0x9fff],
  [0xf900, 0xfaff],
]

/** @type {Record<string, string>} */
const map = {}
for (const [lo, hi] of RANGES) {
  for (let cp = lo; cp <= hi; cp++) {
    const ch = String.fromCodePoint(cp)
    for (const convert of toSimplified) {
      const out = convert(ch)
      // Keep only genuine single-character folds (char dict, not phrase expansion).
      if (out !== ch && [...out].length === 1) {
        map[ch] = out
        break
      }
    }
  }
}

const entries = Object.keys(map)
  .sort()
  .map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`)
  .join('\n')

const body = `// AUTO-GENERATED by scripts/gen-zh-fold-map.mjs â€” do not edit by hand.
// Single-character Traditionalâ†’Simplified fold map (merged HK + TW + TWP OpenCC dictionaries).
// Used by src/lib/zh-fold.ts to normalize text for variant-agnostic local search.
export const ZH_FOLD_MAP: Record<string, string> = {
${entries}
}
`

writeFileSync(OUT, body)
console.log(`Wrote ${Object.keys(map).length} entries to ${OUT}`)
